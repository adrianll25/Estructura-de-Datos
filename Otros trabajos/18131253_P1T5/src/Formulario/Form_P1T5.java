        int num2 = Integer.parseInt(this.jTF_Numero2ParAmigo.getText());
        boolean res;
        res = objRecur.ParAmigos(Integer.parseInt(this.jTF_Numero1.getText().toString()), Integer.parseInt(this.jTF_Numero2.getText().toString()));
        if(res)
            JOptionPane.showMessageDialog(null, "Los numeros son par amigo");
        else
            JOptionPane.showMessageDialog(null, "Los numeros no son par amigos");
