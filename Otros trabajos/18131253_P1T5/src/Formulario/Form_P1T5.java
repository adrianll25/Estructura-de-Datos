    private void jButton_SumaMatricesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SumaMatricesActionPerformed
        int [][]s1 = {{1,2,3},{1,2,3},{1,2,3},{1,2,3}};
        int [][]s2 = {{1,1,1},{2,2,2},{3,3,3},{4,4,4}};
        for (int r = 0; r <s1.length; r++) 
            for (int c = 0; c <s1[0].length; c++){ 
                this.jTable_M1.setValueAt(s1[r][c], r, c);
                this.jTable_M2.setValueAt(s2[r][c], r, c);
        }
        int [][]suma = new int [4][3];
        objRecur.SumaMatrices(s1, s2, suma);
        for (int r = 0; r < s1.length; r++) 
            for (int c = 0; c <s1[0].length; c++) {
                this.jTable_SUMA.setValueAt(suma[r][c], r, c);
        }
    }//GEN-LAST:event_jButton_SumaMatricesActionPerformed

