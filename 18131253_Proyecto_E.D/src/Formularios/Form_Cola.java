    private void jBUT_InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBUT_InsertarActionPerformed
        // TODO add your handling code here:
       String placa = this.jTable_Aleatorio.getValueAt(0, 0).toString();
        String marca = this.jTable_Aleatorio.getValueAt(0, 1).toString();
        int modelo = Integer.parseInt(this.jTable_Aleatorio.getValueAt(0,2).toString());
        String color = this.jTable_Aleatorio.getValueAt(0, 3).toString();
        double precio = Double.parseDouble(this.jTable_Aleatorio.getValueAt(0, 4).toString());
        
        objAutomovil = new Automovil(placa,marca,modelo,color,precio);
        objCola.Insertar(objAutomovil);
        objCola.Mostrar(jTable_Colas);
    }//GEN-LAST:event_jBUT_InsertarActionPerformed

