    private void repintarArbol() {
        this.jDesktopPane.removeAll();
        Rectangle tamaño = this.JPanelArbol.getBounds();
        this.JPanelArbol = null;
        this.JPanelArbol = new JInternalFrame("Representación gráfica", true);
        this.jDesktopPane.add(this.JPanelArbol, JLayeredPane.DEFAULT_LAYER);
        this.JPanelArbol.setVisible(true);
        this.JPanelArbol.setBounds(tamaño);
        this.JPanelArbol.setEnabled(false);
        this.JPanelArbol.add(this.Simulador.getDibujo(), BorderLayout.CENTER);
    }
    
