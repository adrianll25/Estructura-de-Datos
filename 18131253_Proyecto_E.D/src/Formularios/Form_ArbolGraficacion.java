    private void JBut_InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBut_InsertarActionPerformed
        try {
            int dato = Integer.parseInt(jText_Dibujo.getText());
            if (this.Simulador.insertar(dato)) {
                JOptionPane.showMessageDialog(null, "El dato fue insertado correctamente", " ...", 1);
                this.inicializar(true);

                complementos();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se pudo insertar el dato", "Intenta de nuevo...", 0);

        }
    }//GEN-LAST:event_JBut_InsertarActionPerformed

