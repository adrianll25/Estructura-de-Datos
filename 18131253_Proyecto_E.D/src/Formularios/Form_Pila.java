    private void JBUT_PushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBUT_PushActionPerformed
        // TODO add your handling code here:
       String Placa=this.jTableAleatorios.getValueAt(0, 0).toString();
       String Marca=this.jTableAleatorios.getValueAt(0, 1).toString();
       int Modelo= Integer.parseInt(this.jTableAleatorios.getValueAt(0,2).toString());
       String Color= this.jTableAleatorios.getValueAt(0, 3).toString();
       double Precio= Double.parseDouble(this.jTableAleatorios.getValueAt(0,4).toString());
        
       objAuto = new Automovil(Placa, Marca, Modelo, Color, Precio);
       objPila.push(objAuto);
       objPila.MostrarPila(jTablePilas);
      
    }//GEN-LAST:event_JBUT_PushActionPerformed

