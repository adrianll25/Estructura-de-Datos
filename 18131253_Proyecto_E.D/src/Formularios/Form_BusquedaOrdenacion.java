    private void jBut_BInariaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBut_BInariaActionPerformed
        // TODO add your handling code here:
         if(this.OPrincipal==null){
            JOptionPane.showMessageDialog(this, "Antes de ordenar debe de ingresar \n un vector con sus datos");
        }
         else
         {
             int dato =Integer.parseInt(JOptionPane.showInputDialog("Ingrese el dato a buscar: "));
             int[] vector_Ordenado = this.OPrincipal.Inserccion(OPrincipal.getArreglo(), 0, OPrincipal.Longitud-1);
             
             int pos = this.OPrincipal.busquedaBinaria(vector_Ordenado, dato);
             
             if(pos==-1){
                 jTextArea_Binaria.setText("No se encontro el dato: "+ dato + " en el vector.");
             }
             else{
                  jTextArea_Binaria.setText(this.OPrincipal.Imprimir(vector_Ordenado) + "\n" + "posicion: "+ pos );
             }
         }
    }//GEN-LAST:event_jBut_BInariaActionPerformed

