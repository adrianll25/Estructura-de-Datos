    private void jMenuAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuAbrirActionPerformed
        // TODO add your handling code here:
       if(Seleccionar.showDialog(null, "Abrir")==JFileChooser.APPROVE_OPTION){
           archivo = Seleccionar.getSelectedFile();
           if(archivo.canRead()){
               if(archivo.getName().endsWith("txt")){
                   String documento = Abrir(archivo);
                     this.jTextAreaDatos.setText(documento);
                     this.jTextArea_Inserccion.setText(documento);
                     this.jTextArea_Merge.setText(documento);
                     this.jTextTama単o.setText(documento);
        
               }else{
                   JOptionPane.showMessageDialog(null, "Archivo Incomaptiible");
               }
           }
       }

    }//GEN-LAST:event_jMenuAbrirActionPerformed

    private void jMenuGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuGuardarActionPerformed
    JOptionPane.showMessageDialog(null, "Recuerda poner .txt al final ;)");
        if(Seleccionar.showDialog(null, "Guardar")==JFileChooser.APPROVE_OPTION){
            archivo=Seleccionar.getSelectedFile();
            if(archivo.getName().endsWith("txt")){
                String Documento = this.jTextAreaDatos.getText();
                String Mensaje = Guardar(archivo, Documento);
                if(Mensaje!= null){
                JOptionPane.showMessageDialog(null, Mensaje);
            }
                else
                {
                   JOptionPane.showMessageDialog(null, "Archivo No compatible"); 
                    }
            }
            else{
                JOptionPane.showMessageDialog(null, "Guardad Documento de Texto");
            }
        }

    }//GEN-LAST:event_jMenuGuardarActionPerformed

