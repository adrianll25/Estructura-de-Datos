    public String Abrir(File archivo){
        String Documento = "";
        try{
            entrada = new FileInputStream(archivo);
            int ascci;
            while((ascci=entrada.read())!=-1){
                char caracter = (char)ascci;
                Documento+=caracter;
                
                
            }
            
        }catch (Exception e){     
        }
        return Documento;
    }
    
    public String Guardar(File archivo, String Documento){
        String mensaje = "";
        try{
            Salida = new FileOutputStream(archivo);
            byte[] bytxt = Documento.getBytes();
            Salida.write(bytxt);
            mensaje = "Archivo Guardado!!";
            
        }catch (Exception e){
        }
        return mensaje;
    }
    
    private void jBut_IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBut_IngresarActionPerformed
        // TODO add your handling code here:
        if(jTextTama単o.getText().length()>0)
        {
            
            Ordenamientos OIngreso = new Ordenamientos();
            
            
            int Longitud_Vector = Integer.parseInt(jTextTama単o.getText());
            OIngreso.setLongitud(Longitud_Vector);
            OIngreso.InicializadorVector();
            
            for(int i=0; i < OIngreso.Longitud; i++)
            {
                String dato = JOptionPane.showInputDialog("Ingrese el dato para la posicion[ " + i + " ] del vector");
                if(!OIngreso.esNumero(dato)){
                    JOptionPane.showMessageDialog(this, "El Valor no es numerico, intente de nuevo");
                    this.jBut_LimpiarActionPerformed(evt);
                    return;
                    
                }
                OIngreso.Insertar(i, Integer.valueOf(dato));
            }
            jTextAreaDatos.setText(OIngreso.imprimir());
            this.OPrincipal = new Ordenamientos(Longitud_Vector);
            this.OPrincipal.setArreglo(OIngreso.getArreglo());
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Debe de ingresar el tama単o del vector");
        }
    }//GEN-LAST:event_jBut_IngresarActionPerformed

