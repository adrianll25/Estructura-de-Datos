    private void JBUT_ColaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBUT_ColaActionPerformed
        // TODO add your handling code here:
            int Traer = Integer.parseInt(Form_Cola.JTF_Dimension.getText().toString());
            for(int i=0; i<Traer; i++){
            String PlacaCola = Form_Cola.jTable_Colas.getValueAt(0, i).toString();
            String MarcaCola = Form_Cola.jTable_Colas.getValueAt(1, i).toString();
            int ModeloCola = Integer.parseInt(Form_Cola.jTable_Colas.getValueAt(2, i).toString());
            String ColorCola = Form_Cola.jTable_Colas.getValueAt(3, i).toString();
            double PrecioCola = Integer.parseInt(Form_Cola.jTable_Colas.getValueAt(2, i).toString());
            
            objAuto = new Automovil(PlacaCola, MarcaCola, ModeloCola, ColorCola, PrecioCola);
            objLE.InsFin(objAuto);
            objLE.MostrarLE(this.jTable_ListaEnlazada);
        }
    }//GEN-LAST:event_JBUT_ColaActionPerformed

