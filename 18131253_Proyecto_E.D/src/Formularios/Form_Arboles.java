    private void jButton_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_eliminarActionPerformed
        int NumProceso = Integer.parseInt(this.jTextField_NumProceso.getText());
        ArbolNodo []nodoEnc = new ArbolNodo[1];
        if(objArbol.BuscarNodo(NumProceso, nodoEnc))
        objArbol.eliminar(nodoEnc);
        else
        JOptionPane.showMessageDialog(null,"El nodo no se encuentra en el arbol :(");
    }//GEN-LAST:event_jButton_eliminarActionPerformed

    private void jButton_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_BuscarActionPerformed
        int NumProceso = Integer.parseInt(this.jTextField_NumProceso.getText());
        ArbolNodo []nodoEnc = new ArbolNodo[1];
        if(objArbol.BuscarNodo(NumProceso, nodoEnc)){
            JOptionPane.showMessageDialog(null,"Se encontro el nodo :)");
        }
        else{
            JOptionPane.showMessageDialog(null, "No se ha encontrado el nodo :( ");
        }
    }//GEN-LAST:event_jButton_BuscarActionPerformed

